def decode_letter(code:str):
    code = code.lower()
    if code in MorseCode:
        return str(MorseCode[code])
    return "?"

def decode_word(code:str):
    letters = code.split("/")
    res_word = ""
    for letter in letters:
        res_word += decode_letter(letter)
    return res_word